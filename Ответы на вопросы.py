#2. Что из себя представляют циклы в python, какие конструкции языка используються? В чем их отличие?

#Ответ:

#Циклы в Python представляют собой управляющие структуры, которые выполняют повторяющиеся действия до тех пор, пока выполняется определенное условие. В Python существуют два основных вида циклов: циклы с предусловием (while) и циклы с постусловием (for).

#Циклы с предусловием (while) используются для выполнения блока кода до тех пор, пока определенное условие истинно.
#Циклы с постусловием (for) используются для выполнения блока кода для каждого элемента в итерируемом объекте (например, список, кортеж, строка).
#Отличие между циклами while и for заключается в том, что цикл while выполняется, пока условие истинно, в то время как цикл for выполняется для каждого элемента в итерируемом объекте.

#3. Что из себя представляют списки, кортежи, словари?

#Ответ:

#Списки, кортежи и словари являются структурами данных в языке программирования Python.

#Список (list) представляет собой упорядоченную коллекцию элементов, которые могут быть любого типа. Элементы списка могут быть изменяемыми, что позволяет добавлять, удалять или изменять элементы списка.

#Кортеж (tuple) также представляет собой упорядоченную коллекцию элементов, но в отличие от списка, кортеж является неизменяемым. Это означает, что после создания кортежа его элементы нельзя изменить.

#Словарь (dictionary) представляет собой коллекцию пар "ключ-значение". Ключи словаря должны быть уникальными и неизменяемыми, а значения могут быть любого типа. Словари позволяют эффективно хранить и извлекать информацию по ключу.